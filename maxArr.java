public class maxArr {
    public static void main(String[] args) {
        int [] m = {1,2,55,3,4,5,6,7};
        int max =0;
        for (int i = 0 ; i < m.length ; i++){
            if (m[i] > max) {
                max = m[i];
            }}

            System.out.print(max);
    }
}
